     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cost = str_replace(",", ".", $request->input('cost'));
        $final =    substr($request->input('final'), 6, 4) . '-' .
                    substr($request->input('final'), 3,2) . '-' .
                    substr($request->input('final'), 0,2);
        $user = Auth::id();
        $request->merge([
            'cost' => $cost,
            'final' => $final,
            'user_id' =>$user
        ]);
        $project = Project::create($request->all());
        $project_pagination = DB::table('projects')->paginate(5);
        return redirect('projects/create')
            ->with('project_pagination', $project_pagination);
    }

